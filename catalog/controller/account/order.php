<?php
class ControllerAccountOrder extends Controller {

	private $error = array();
	protected function validate() {
		if ((trim($this->request->post['comment']) == '')) {
			$this->error['error_faulty_message'] = $this->language->get('error_faulty_message');
		}
		return !$this->error;
	}

